cube_list = []
for i in range(1, 1001, 2):
    cube_list.append(i**3)
sum_of_cubes = 0
for num in cube_list:
    a = num // 1000000000
    b = num % 1000000000 // 100000000
    c = num % 1000000000 % 100000000 // 10000000
    d = num % 1000000000 % 100000000 % 10000000 // 1000000
    e = num % 1000000000 % 100000000 % 10000000 % 1000000 // 100000
    f = num % 1000000000 % 100000000 % 10000000 % 1000000 % 100000 // 10000
    g = num % 1000000000 % 100000000 % 10000000 % 1000000 % 100000 % 10000 // 1000
    h = num % 1000000000 % 100000000 % 10000000 % 1000000 % 100000 % 10000 % 1000 // 100
    i = num % 1000000000 % 100000000 % 10000000 % 1000000 % 100000 % 10000 % 1000 % 100 // 10
    j = num % 1000000000 % 100000000 % 10000000 % 1000000 % 100000 % 10000 % 1000 % 100 % 10
    check_sum = (a + b + c + d + e + f + g + h + i + j)
    if check_sum % 7 == 0:
        sum_of_cubes += num
print(sum_of_cubes)

